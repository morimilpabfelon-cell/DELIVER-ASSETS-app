import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const exists = (file) => fs.existsSync(path.join(root, file))
const checks = []
const check = (name, value) => checks.push({ name, ok: Boolean(value) })

const pkg = JSON.parse(read('package.json'))
const app = JSON.parse(read('app.json')).expo
const context = read('context/AppContext.tsx')
const coordination = read('data/coordination.ts')
const operations = read('data/operations.ts')
const conversation = read('components/OrderConversation.tsx')
const receipt = read('components/OperationReceiptCard.tsx')
const layout = read('app/_layout.tsx')
const backend = read('services/backend.ts')
const persistence = read('services/persistence.ts')
const chatMedia = read('services/chatMedia.ts')
const hub = read('dev/sync-hub/server.mjs')

const adminScreen = read('app/admin.tsx')
const chatRoute = read('app/order-chat/[id].tsx')
const receiptRoute = read('app/receipt/[id].tsx')
const start = read('scripts/start-control.ps1')

check('Control 2.3.0', pkg.version === '2.3.0' && app.version === '2.3.0')
check('Android versionCode 23', app.android.versionCode === 23)
check('iOS buildNumber 23', app.ios.buildNumber === '23')
check('paquete Control aislado', app.android.package === 'com.deliverassets.control')
check('schema persistente 7', persistence.includes('APP_SCHEMA_VERSION = 7'))
check('migración desde schema 6', persistence.includes('[1, 2, 3, 4, 5, 6, APP_SCHEMA_VERSION]'))
check('dominio coordinación presente', coordination.includes('OperationCoordination') && coordination.includes('OperationReceipt'))
check('operación conserva coordinación', operations.includes('coordination?: OperationCoordination'))
check('Control solo ve escalados', context.includes('coordination.escalated || adminJoined'))
check('Control exige ingreso explícito', context.includes('joinOperationAsAdmin') && context.includes('Control debe unirse'))
check('Control no entra en chat normal', context.includes('solo puede entrar cuando existe una solicitud de ayuda'))
check('historial cerrado no se modifica', context.includes('Historial escalado abierto en modo solo lectura'))
check('Control envía texto', context.includes('sendOperationText') && context.includes("'admin'"))
check('Control envía evidencia', context.includes('sendOperationImage') && chatMedia.includes('/v1/media/chat'))
check('panel lista escalaciones', adminScreen.includes('CONVERSACIONES ESCALADAS') && adminScreen.includes('CASOS QUE REQUIEREN CONTROL'))
check('panel muestra pago', adminScreen.includes('PAGO CONFIRMADO') && adminScreen.includes('EFECTIVO PENDIENTE'))
check('ruta de chat Control', chatRoute.includes('selfRole="admin"') && chatRoute.includes('joinOperationAsAdmin'))
check('ruta de boleta Control', receiptRoute.includes('AUDITORÍA FINANCIERA DEL PEDIDO'))
check('Control ve boleta completa', receipt.includes("role === 'rider'") && receipt.includes('CLIENTE'))
check('cámara y galería', conversation.includes('launchCameraAsync') && conversation.includes('launchImageLibraryAsync'))
check('mensajes propios', layout.includes('FeedbackProvider'))
check('dependencias nativas', pkg.dependencies['expo-image-picker'] && pkg.dependencies['expo-file-system'])
check('Hub Fase 3', hub.includes('rider-chat-v1') && hub.includes('control-intervention-v1'))
check('arranque individual 8084', start.includes('tcp:8084') && start.includes('control-intervention-v1'))
check('cliente Control actualizado', backend.includes('2.3.0-control'))
check('sin pantallas de otros roles', !exists('app/rider.tsx') && !exists('app/business.tsx') && !exists('app/(tabs)'))

const failed = checks.filter((item) => !item.ok)
for (const item of checks) console.log(`${item.ok ? 'PASS' : 'FAIL'} · ${item.name}`)
console.log(`\n${checks.length - failed.length}/${checks.length} verificaciones de Fase 3 aprobadas.`)
if (failed.length) process.exit(1)
