import fs from 'node:fs'
import path from 'node:path'
const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const pkg = JSON.parse(read('package.json'))
const app = JSON.parse(read('app.json')).expo
const cart = read('data/cart.ts')
const context = read('context/AppContext.tsx')
const cartPersistence = read('services/cartPersistence.ts')
const photo = read('services/profilePhoto.ts')
const avatar = read('components/ProfileAvatar.tsx')
const product = read('app/product/[id].tsx')
const profile = read('app/profile.tsx')
const cartScreen = read('app/cart.tsx')
const checks = []
const check = (name, condition) => checks.push({ name, ok: Boolean(condition) })

check('versión 2.6.0 sincronizada', pkg.version === '2.6.0' && app.version === '2.6.0')
check('builds 26 sincronizados', app.android.versionCode === 26 && app.ios.buildNumber === '26')
check('carrito usa firma exacta', cart.includes('signature: string') && cart.includes('addOrMergeCartLine'))
check('productos diferentes se anexan', cart.includes('return [...normalizedCurrent, nextLine]'))
check('Context usa motor de carrito', context.includes('addOrMergeCartLine(current, nextLine)'))
check('borrador dedicado es autoridad al restaurar', context.includes('The independent cart draft is authoritative'))
check('escrituras del carrito están serializadas', cartPersistence.includes('let writeChain') && cartPersistence.includes('flushCartWrites'))
check('cart schema v4 migra versiones anteriores', cartPersistence.includes('CART_SCHEMA_VERSION = 4') && cartPersistence.includes('@deliver-assets/customer-cart-v3') && cartPersistence.includes('@deliver-assets/customer-cart-v2'))
check('borrador tiene checksum con comercio activo', cartPersistence.includes('checksumFor(cart, promo, activeStoreId)'))
check('foto usa archivo único', photo.includes('avatar-${suffix}') && photo.includes('Math.random'))
check('foto vieja se elimina después de copiar', photo.indexOf('source.copy(target)') < photo.indexOf('cleanupProfilePhotos(target.uri)'))
check('foto copiada se valida', photo.includes('target.size <= 0'))
check('avatar remonta al cambiar URI', avatar.includes('key={photoUri}'))
check('avatar vuelve a iniciales si falla', avatar.includes('onError') && avatar.includes('failedUri'))
check('perfil envía URI anterior al reemplazo', profile.includes('persistProfilePhoto(asset.uri, asset.mimeType, profile.photoUri)'))
check('ruta inválida no agrega el primer producto', !product.includes('?? stores[0]') && product.includes('PRODUCTO NO ENCONTRADO'))
check('otro comercio se agrega sin alerta destructiva', !product.includes('Conservar carrito') && !product.includes('Reemplazar'))
check('selector muestra precio por comercio', cartScreen.includes('summary.total.toFixed(2)'))
check('checkout solo usa carrito activo', context.includes('cartLines.filter((line) => line.storeId === resolvedActiveCartStoreId)'))
check('confirmar pedido conserva otros carritos', context.includes('current.filter((line) => line.storeId !== resolvedActiveCartStoreId)'))
check('dock de tienda abre su carrito contextual', read('components/CartDock.tsx').includes('contextual') && read('app/store/[id].tsx').includes('<CartDock storeId={store.id}/>'))

for (const item of checks) console.log(`${item.ok ? 'PASS' : 'FAIL'} · ${item.name}`)
const failed = checks.filter((item) => !item.ok)
console.log(`\n${checks.length - failed.length}/${checks.length} verificaciones de regresión aprobadas.`)
if (failed.length) process.exit(1)
