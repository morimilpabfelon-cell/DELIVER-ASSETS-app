import fs from 'node:fs'
import path from 'node:path'

const root = process.cwd()
const read = (file) => fs.readFileSync(path.join(root, file), 'utf8')
const checks = []
const check = (name, ok) => checks.push({ name, ok: Boolean(ok) })

const pkg = JSON.parse(read('package.json'))
const app = JSON.parse(read('app.json')).expo
const catalog = read('data/catalog.ts')
const domain = read('data/merchantCatalog.ts')
const context = read('context/AppContext.tsx')
const home = read('app/(tabs)/index.tsx')
const search = read('app/(tabs)/search.tsx')
const store = read('app/store/[id].tsx')
const product = read('app/product/[id].tsx')
const favorites = read('app/favorites.tsx')
const media = read('components/CustomerBusinessMedia.tsx')
const backend = read('services/backend.ts')
const hub = read('dev/sync-hub/server.mjs')
const start = read('scripts/start-customer.ps1')

check('Customer 2.8.0 sincronizada', pkg.version === '2.8.0' && app.version === '2.8.0')
check('Android versionCode 28', app.android.versionCode === 28)
check('cinco universos visibles', home.includes('5 UNIVERSOS') && catalog.includes("'tiendas'"))
check('catálogo compartido', domain.includes('customerVisibleProducts') && context.includes('getStoreProducts'))
check('perfil público compartido', context.includes('getStorePublicProfile'))
check('producto dinámico', context.includes('getStoreProduct'))
check('logo público con fallback', media.includes('CustomerBusinessLogo') && media.includes('onError'))
check('portada pública con fallback', media.includes('CustomerBusinessCover') && media.includes('coverUrl'))
check('inicio muestra identidad publicada', home.includes('CustomerBusinessCover') && home.includes('CustomerBusinessLogo'))
check('búsqueda usa catálogo dinámico', search.includes('getStoreProducts') && search.includes('product.variants'))
check('favoritos usan identidad pública', favorites.includes('CustomerBusinessLogo'))
check('comercio usa arquitectura por tipo', store.includes('templateForStore') && store.includes('template.customerMode'))
check('comercio no usa store.products', !store.includes('store.products.map'))
check('detalle usa producto compartido', product.includes('getStoreProduct'))
check('variantes obligatorias', product.includes('Elige una variante') && product.includes('selectedVariantId'))
check('precio de variante llega al carrito', product.includes('variantOption') && product.includes('cartProduct'))
check('farmacia tiene aviso específico', product.includes("store.businessType === 'pharmacy'"))
check('checkout detecta precio cambiado', context.includes('cambió de precio o información'))
check('repetición usa catálogo actual', context.includes('getStoreProduct(store.id, item.productId)'))
check('promoción Tiendas', context.includes("'TIENDA7'"))
check('cliente backend v2.7', backend.includes('2.8.0-customer'))
check('hub conoce identidad y catálogo', hub.includes('business-media-v1') && hub.includes('catalog-v2'))
check('arranque rechaza hub antiguo', start.includes('business-media-v1') && start.includes('catalog-v2'))

const failed = checks.filter((item) => !item.ok)
for (const item of checks) console.log(`${item.ok ? 'PASS' : 'FAIL'} · ${item.name}`)
console.log(`\n${checks.length - failed.length}/${checks.length} verificaciones de Fase 1 Customer aprobadas.`)
if (failed.length) process.exit(1)
